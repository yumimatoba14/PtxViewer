#include "YmRgba.h"

using namespace std;
using namespace Ymcpp;

////////////////////////////////////////////////////////////////////////////////

YmRgba4b::YmRgba4b(int r, int g, int b)
{
	m_rgba = (0xFF & r) | (0xFF & g) << 8 | (0xFF & b) << 16;
}

